<template>
  <ChutierPane @close="$emit('close')">
    <div class="_mediaFocus" @click="last_clicked = false">
      <div class="_fileStack">
        <transition-group tag="div" class="_itemsList" name="listComplete">
          <div
            class="u-sameRow"
            v-for="(file, index) in files"
            :key="file.$path"
            @click.stop="last_clicked = file.$path"
          >
            <div class="_removeFile">
              <sl-icon-button
                name="dash-square-dotted"
                @click="removeFromSelection(file.$path)"
              />
            </div>

            <select
              class="is--dark _changeOrderSelect"
              :value="index + 1"
              @change="changeMediaOrder(index, +$event.target.value - 1)"
            >
              <option
                v-for="(a, i) in new Array(files.length).fill(null)"
                :key="i + 1"
                v-text="i + 1"
              />
            </select>
            <ChutierItem
              :file="file"
              :is_clicked="last_clicked === file.$path"
              :is_selected="false"
              :context="'stack'"
              @unclicked="last_clicked = false"
            />
          </div>
        </transition-group>
      </div>

      <div class="_fields">
        <small>
          <input
            class="is--dark"
            type="datetime-local"
            v-model="date_created_corrected"
            step="1"
          />
        </small>

        <span class="u-instructions">
          {{ $t("complete_or_correct_title_kw") }}
        </span>

        <input
          type="text"
          class="is--dark"
          required
          v-model="title"
          :placeholder="$t('title')"
          @keydown.esc.prevent="$emit('close')"
        />
        <KeywordsField
          :can_edit="true"
          :keywords.sync="keywords"
          @cancelEdit="$emit('close')"
        />
        <textarea
          class="is--dark _descriptionField"
          v-model="description"
          :placeholder="$t('description')"
          @keydown.esc.prevent="$emit('close')"
        />
      </div>

      <div class="_shareBtn">
        <transition name="scaleInFade" mode="out-in">
          <button
            type="button"
            v-if="shared_space_path"
            :key="share_button_is_enabled"
            class="u-buttonLink"
            :disabled="!share_button_is_enabled"
            @click="shareButtonClicked"
          >
            {{ $t("publish") }}&nbsp;
            <sl-icon name="arrow-right-square" style="font-size: 1rem" circle />
          </button>
        </transition>
      </div>
    </div>
  </ChutierPane>
</template>
<script>
import ChutierPane from "@/components/chutier/ChutierPane.vue";
import KeywordsField from "@/components/KeywordsField.vue";
import ChutierItem from "@/components/ChutierItem.vue";

export default {
  props: {
    files: Array,
    shared_space_path: String,
  },
  components: {
    ChutierPane,
    KeywordsField,
    ChutierItem,
  },
  data() {
    return {
      last_clicked: undefined,
      title: "",
      description: "",
      keywords: [],
      date_created_corrected: "",
    };
  },
  created() {
    const file_dates = this.files.map(
      (f) => f.date_created_corrected || f.$date_created || f.$date_uploaded
    );
    file_dates.sort((a, b) => +new Date(b) - +new Date(a));
    this.date_created_corrected = this.datetimeLocal(file_dates[0]);
  },
  mounted() {},
  beforeDestroy() {},
  watch: {},
  computed: {
    share_button_is_enabled() {
      return this.title.length > 0 && this.keywords.length > 0;
    },
  },
  methods: {
    changeMediaOrder(old_position, new_position) {
      let files_path = this.files.map((f) => f.$path);
      function array_move(arr, old_index, new_index) {
        if (new_index >= arr.length) {
          var k = new_index - arr.length + 1;
          while (k--) {
            arr.push(undefined);
          }
        }
        arr.splice(new_index, 0, arr.splice(old_index, 1)[0]);
        return arr; // for testing
      }
      array_move(files_path, old_position, new_position);
      this.$emit("updateFocusedMedia", files_path);
    },
    removeFromSelection(path) {
      let files_path = this.files.map((f) => f.$path);
      files_path = files_path.filter((fp) => fp !== path);
      this.$emit("updateFocusedMedia", files_path);
    },
    async shareButtonClicked() {
      const path_to_destination_folder = this.shared_space_path;

      // get fields
      this.title, this.description, this.keywords;

      const additional_meta = {
        title: this.title,
        description: this.description,
        keywords: this.keywords,
        date_created_corrected: this.date_created_corrected,
        requested_slug: "stack",
        is_stack: true,
        stack_files_metas: [],
        $admins: [this.connected_as.$path],
      };
      const stack_meta_filename = await this.$api
        .uploadFile({
          path: path_to_destination_folder,
          additional_meta,
        })
        .catch((err) => {
          this.$alertify.delay(4000).error(err);
          throw err;
        });

      const file_metas = [];
      // copy each file to shared space, with a flag to indicate they are part of a stack
      for (const file of this.files) {
        const file_meta = await this.$api.copyFile({
          path: file.$path,
          path_to_destination_folder,
          new_meta: {
            $admins: [this.connected_as.$path],
            belongs_to_stack: stack_meta_filename,
          },
        });
        file_metas.push(file_meta);
        await this.$api.deleteItem({ path: file.$path });
      }

      // add file metas to stack meta
      await this.$api.updateMeta({
        path: path_to_destination_folder + "/" + stack_meta_filename,
        new_meta: {
          stack_files_metas: file_metas,
        },
      });

      this.$emit("stackCreated");
    },
  },
};
</script>
